from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from dpylib.common.contextext import ExtendedContext


async def localize(ctx: 'ExtendedContext', msgkey: str, *args) -> str:
    lang = await ctx.ext.get_lang()
    return lang[msgkey].format(*args)
